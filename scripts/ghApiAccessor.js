    let updateRepos = function (projects) {
        let projInfos = [];
        projects.forEach(project => {
            // do not include forked projects
            if (!project.fork)
                projInfos.push({ name: project.name })
        });

        new Vue({
            el: '#projectsContainer',
            data: {
                items: projInfos
            }
        });
    }

    let updateLanguages = function (projects) {
        let langs = {};
        let valSum = 0;
        projects.forEach(project => {
            for (let key in project.languages) {
                let value = project.languages[key];
                if (!(key in langs))
                    langs[key] = 0;
                langs[key] += value;
                valSum += value;
            }
        });

        // if requests failed, use backup
        if (Object.keys(langs).length == 0) {
            langs = LANGBACKUP;
            for (let key in langs) {
                valSum += langs[key];
            }
        }

        let languages = [];
        let ind = 0;
        for (let key in langs) {
            languages.push({
                id: "languageBarNo" + ind,
                name: key,
                value: langs[key]
            });
            ind++;
        }

        // sorting descending
        languages.sort(function (first, second) {
            return second.value - first.value;
        });

        // generating html
        new Vue({
            el: '#languagesContainer',
            data: {
                items: languages
            }
        });

        // setting progress
        languages.forEach(language => {
            let progressBar = $("#" + language.id);
            let proc = Math.round(language.value / valSum * 100) + 1;

            // logarythmic scale
            let s = Math.round(Math.log(valSum) * Math.log(valSum));
            let v = Math.round(Math.log(language.value) * Math.log(language.value));

            progressBar.progress("set total", s);
            progressBar.progress("set progress", v);
        });
        console.log(languages);
        console.log(langs);
    }

